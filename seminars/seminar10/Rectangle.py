class Rectangle:

    def __init__(self, a, b=None):
        self.a = a
        self.b = a if (b is None) else b

    def get_area(self):
        return self.a * self.b

    def get_perimetr(self):
        return (self.a + self.b) * 2


if __name__ == '__main__':
    rect = Rectangle(4, 5)
    print(f'{rect.get_area()}\t{rect.get_perimetr()}')
